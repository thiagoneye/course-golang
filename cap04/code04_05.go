/*
Comments:
Numerical Systems == Numerical Bases
%d == Format Verb to Decimal Number
%b == Format Verb to Binary Number
%#x == Format Verb to Hexadecimal Number
*/

package main

import "fmt"

func main() {
    i := 100
    fmt.Printf("%d \t %b \t %#x \n", i, i, i)
}
